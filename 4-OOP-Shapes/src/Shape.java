
public abstract class Shape{
	protected Point center = new Point();
	public Shape(){
	}
	public Shape(Point center){
		this.center = center;
	}

	public Point getCenter(){
		return this.center;
	}
	public void setCenter(int x,int y){
		this.center.setx(x);
		this.center.sety(y);
	}
	public abstract int[][] getBounds();
}
